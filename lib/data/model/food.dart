class Food {
  final String id;
  final String name;
  final int calories;
  final double proteins;
  final double fats;
  final double carbs;
  final String mealType;
  final String imageUrl;

  Food({
    required this.id,
    required this.name,
    required this.calories,
    required this.proteins,
    required this.fats,
    required this.carbs,
    required this.mealType,
    required this.imageUrl,
  });
}
